# LOOPING

# for each i from list[]
for i in [2, 4, 6, 8, 10]:
    # print i = value from list
    print('i = ', i)
print()

# for each i in range of 10 values (0-9)
for i in range(10):
    print('i = ', i)
print()

# for each i in range of 2-9
for i in range(2, 10):
    print('i = ', i)
print()

# Test value is odd
# set i
i = 2
# modulus 2 of i is equal to 0 returns True
print(i % 2 == 0)
